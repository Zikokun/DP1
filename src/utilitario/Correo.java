package utilitario;

/**
 *
 * @author FranciscoMartin
 */
public class Correo {
    private String usuarioCorreo;
    private String contrasenha;
    private String rutaArch;
    private String nombArch;
    private String destino;
    private String asunto;
    private String mensaje;

    /**
     * @return the usuarioCorreo
     */
    public String getUsuarioCorreo() {
        return usuarioCorreo;
    }

    /**
     * @param usuarioCorreo the usuarioCorreo to set
     */
    public void setUsuarioCorreo(String usuarioCorreo) {
        this.usuarioCorreo = usuarioCorreo;
    }

    /**
     * @return the contrasenha
     */
    public String getContrasenha() {
        return contrasenha;
    }

    /**
     * @param contrasenha the contrasenha to set
     */
    public void setContrasenha(String contrasenha) {
        this.contrasenha = contrasenha;
    }

    /**
     * @return the rutaArch
     */
    public String getRutaArch() {
        return rutaArch;
    }

    /**
     * @param rutaArch the rutaArch to set
     */
    public void setRutaArch(String rutaArch) {
        this.rutaArch = rutaArch;
    }

    /**
     * @return the nombArch
     */
    public String getNombArch() {
        return nombArch;
    }

    /**
     * @param nombArch the nombArch to set
     */
    public void setNombArch(String nombArch) {
        this.nombArch = nombArch;
    }

    /**
     * @return the destino
     */
    public String getDestino() {
        return destino;
    }

    /**
     * @param destino the destino to set
     */
    public void setDestino(String destino) {
        this.destino = destino;
    }

    /**
     * @return the asunto
     */
    public String getAsunto() {
        return asunto;
    }

    /**
     * @param asunto the asunto to set
     */
    public void setAsunto(String asunto) {
        this.asunto = asunto;
    }

    /**
     * @return the mensaje
     */
    public String getMensaje() {
        return mensaje;
    }

    /**
     * @param mensaje the mensaje to set
     */
    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }
    
}
